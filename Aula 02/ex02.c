#include <stdio.h>
#include <stdlib.h>

 


int main(void) {
	float soma,notas;
	int i, media;
	printf("Digite a media: \n");
	scanf("%d",&media);
	
	soma == 0;
	for(i=0;i<media;i++){
		
		printf("Nota %d:", i+1);
		scanf("%f",&notas);
		soma += notas;
	}
		printf("\nMedia: %.1f\n", soma/media);
		getch();
}
